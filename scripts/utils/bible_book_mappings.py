def normalize_book_name(book_name: str):
    name = str(book_name).strip()
    alias_code = BOOK_ALIASES.get(name)
    if alias_code:
        return BOOK_BY_CODE.get(alias_code)
    return BOOK_BY_ZH.get(name) or BOOK_BY_EN.get(name) or BOOK_BY_ZH_ABBREV.get(name)


def build_bbb_ccc_vvv(book_name: str, chapter, verse) -> str:
    book = normalize_book_name(book_name)
    if not book:
        raise ValueError(f"Unknown book name: {book_name}")
    return f"{book['code']}-{int(chapter):03d}-{int(verse):03d}"
