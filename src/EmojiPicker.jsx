export default function EmojiPicker({ onEmojiSelect, trigger = '😊', size = 20 }) {
  const [isOpen, setIsOpen] = useState(false)
  const [activeCategory, setActiveCategory] = useState(0)
  const pickerRef = useRef(null)

  useEffect(() => {
    function handleClickOutside(event) {
      if (pickerRef.current && !pickerRef.current.contains(event.target)) {
        setIsOpen(false)
      }
    }
    document.addEventListener('mousedown', handleClickOutside)
    return () => document.removeEventListener('mousedown', handleClickOutside)
  }, [])

  const handleEmojiClick = (emoji) => {
    onEmojiSelect(emoji)
    setIsOpen(false)
  }

  return (
    <div className="emoji-picker-wrapper" ref={pickerRef} style={{ position: 'relative', display: 'inline-block' }}>
      <button
        type="button"
        className="emoji-trigger-btn"
        onClick={() => setIsOpen(!isOpen)}
        style={{
          background: 'none',
          border: 'none',
          cursor: 'pointer',
          fontSize: `${size}px`,
          padding: '4px 6px',
          borderRadius: '8px',
          transition: 'all 0.2s',
          lineHeight: 1,
        }}
        title="插入表情"
      >
        {trigger}
      </button>

      {isOpen && (
        <div
          className="emoji-picker-panel"
          style={{
            position: 'absolute',
            bottom: 'calc(100% + 8px)',
            right: 0,
            zIndex: 1000,
            background: 'rgba(30, 30, 40, 0.98)',
            backdropFilter: 'blur(20px)',
            borderRadius: '16px',
            boxShadow: '0 20px 60px rgba(0,0,0,0.5), 0 0 0 1px rgba(255,255,255,0.1)',
            padding: '12px',
            width: '320px',
            maxHeight: '380px',
            display: 'flex',
            flexDirection: 'column',
          }}
        >
          {/* Category tabs */}
          <div style={{
            display: 'flex',
            gap: '4px',
            marginBottom: '10px',
            paddingBottom: '8px',
            borderBottom: '1px solid rgba(255,255,255,0.1)',
            overflowX: 'auto',
            scrollbarWidth: 'none',
          }}>
            {EMOJI_CATEGORIES.map((cat, idx) => (
              <button
                key={idx}
                type="button"
                onClick={() => setActiveCategory(idx)}
                style={{
                  background: activeCategory === idx ? 'rgba(120,80,200,0.5)' : 'transparent',
                  border: 'none',
                  color: activeCategory === idx ? '#fff' : 'rgba(255,255,255,0.6)',
                  padding: '4px 10px',
                  borderRadius: '8px',
                  fontSize: '12px',
                  cursor: 'pointer',
                  whiteSpace: 'nowrap',
                  transition: 'all 0.2s',
                }}
              >
                {cat.name}
              </button>
            ))}
          </div>

          {/* Emoji grid */}
          <div style={{
            display: 'grid',
            gridTemplateColumns: 'repeat(8, 1fr)',
            gap: '4px',
            overflowY: 'auto',
            padding: '4px',
          }}>
            {EMOJI_CATEGORIES[activeCategory].emojis.map((emoji, idx) => (
              <button
                key={idx}
                type="button"
                onClick={() => handleEmojiClick(emoji)}
                style={{
                  background: 'none',
                  border: 'none',
                  cursor: 'pointer',
                  fontSize: '22px',
                  padding: '6px',
                  borderRadius: '8px',
                  transition: 'all 0.15s',
                  lineHeight: 1,
                }}
                onMouseEnter={(e) => {
                  e.target.style.background = 'rgba(255,255,255,0.15)'
                  e.target.style.transform = 'scale(1.2)'
                }}
                onMouseLeave={(e) => {
                  e.target.style.background = 'none'
                  e.target.style.transform = 'scale(1)'
                }}
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  )
}
